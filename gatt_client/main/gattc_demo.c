void selectPresetSlot(uint8_t presetNum) {

    uint8_t writing_data[16];
    memset(writing_data, 0, sizeof(writing_data));
    writing_data[0] = 0x7e;
    writing_data[1] = 0x42;
    esp_fill_random(&writing_data[2], 4);
    writing_data[6] = presetNum;
    

    for (size_t i = 0; i < PROFILE_NUM; i++){
        if(gls_profile_tab[i].is_connected) {
            ESP_LOGI(GATTS_TAG, "Indicating to remote device");
            esp_ble_gatts_send_indicate(
                    gls_profile_tab[i].gatts_if,
                    gls_profile_tab[i].conn_id,
                    gls_profile_tab[i].char_handle,
                    sizeof(writing_data),
                    writing_data,
                    false
                    );
        }
    }
}

void addRemoteProfile(uint16_t handle) {
    remote_profiles[profile_index] = handle;
    profile_index++;
    if(profile_index >= GATTS_PROFILE_NUM) profile_index = 0;
}
void cleanRemoteProfile() {
    for (size_t i = 0; i < GATTS_PROFILE_NUM; i++) {
        remote_profiles[i] = 0;
    }
    profile_index = 0;
}


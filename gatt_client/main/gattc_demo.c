void gpio_config_func(void)
{
    // ESP_LOGI(TAG, "\nConfiguring input pins...");
    gpio_config_t io_conf = {};
    io_conf.intr_type = GPIO_INTR_NEGEDGE;
    io_conf.mode = GPIO_MODE_INPUT;
    io_conf.pin_bit_mask = ((1ULL << BUT_RESET) | (1ULL << BUT_BLE) | (1ULL << BUT_RESET_HUB6));
    io_conf.pull_down_en = GPIO_PULLDOWN_DISABLE;
    io_conf.pull_up_en = GPIO_PULLUP_ENABLE;
    gpio_config(&io_conf);
    gpio_evt_queue = xQueueCreate(10, sizeof(uint32_t));
    xTaskCreate(gpio_task_button_handler, "gpio_task_button_handle", 2048, NULL, 10, NULL);
    gpio_install_isr_service(0);
    gpio_isr_handler_add(BUT_RESET_HUB6, gpio_isr_handler, (void *)BUT_RESET_HUB6);
    gpio_isr_handler_add(BUT_RESET, gpio_isr_handler, (void *)BUT_RESET);
    gpio_isr_handler_add(BUT_BLE, gpio_isr_handler, (void *)BUT_BLE);
}

